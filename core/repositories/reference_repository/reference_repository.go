type IReferenceRepository interface {
	Add(data pos_models.Reference) (repository pos_models.ReferenceRepositoryResponse)
	Update(id uint, date pos_models.Reference) (repository pos_models.ReferenceRepositoryResponse)
	AddOrUpdate(id int, data pos_models.Reference) (repository pos_models.ReferenceRepositoryResponse)
	GetByID(id uint) (repository pos_models.ReferenceRepositoryResponse)
	GetByName(param string) (repository pos_models.ReferenceRepositoryResponse)
	GetByStage(param string) (repository pos_models.ReferenceRepositoryResponse)
	GetByType(param string) (repository pos_models.ReferenceRepositoryResponse)
	GetByDate(param string) (repository pos_models.ReferenceRepositoryResponse)
	GetByStatus(param int) (repository pos_models.ReferenceRepositoryResponse)
	GetByEnabled(param int) (repository pos_models.ReferenceRepositoryResponse)
	GetByLocale(param string) (repository pos_models.ReferenceRepositoryResponse)
	CheckIFExists(id uint) (repository pos_models.ReferenceRepositoryResponse)
	GetAll() (repository pos_models.ReferenceRepositoryResponse)
	Delete(id uint) (repository pos_models.ReferenceRepositoryResponse)
}
type ReferenceRepository struct {
	GormDB *gorm.DB
}

func New() *ReferenceRepository {

	return &ReferenceRepository{
		GormDB: pos_databases.New().DBConnection(),
	}
}

func (r *ReferenceRepository) Add(data *pos_models.Reference) (repository pos_models.ReferenceRepositoryResponse) {
	repository.RepositoryErrorResponse = new(error_models.ErrorModel)

	if data == nil {

		repository.Error = error_constants.NilDataErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Reference = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.ReferenceList = make([]*pos_models.Reference, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	if data.ID == uint(0) {

		repository.Error = error_constants.MissingIdErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Reference = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.ReferenceList = make([]*pos_models.Reference, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	data.Stage = "added"
	data.CreatedAt = time.Now()
	log.Println(data.ToJSON())
	err := r.GormDB.Save(&data).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Reference = data
		repository.StatusCode = http.StatusInternalServerError
		repository.ReferenceList = make([]*pos_models.Reference, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}
	err = r.GormDB.Find(&repository.ReferenceList, "id=?", data.ID).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Reference = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.ReferenceList = make([]*pos_models.Reference, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	if len(repository.ReferenceList) > 0 {
		repository.StatusCode = http.StatusCreated
		repository.Message = "Success"
		repository.RepositoryStatus = true
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		r.GormDB.Find(&repository.Reference, "id=?", repository.ReferenceList[0].ID)

	} else {
		repository.StatusCode = http.StatusNoContent
		repository.Message = "No Content"
		repository.RepositoryStatus = false
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
	}
	return repository
}
func (r *ReferenceRepository) Update(id uint, data *pos_models.Reference) (repository pos_models.ReferenceRepositoryResponse) {
	repository.RepositoryErrorResponse = new(error_models.ErrorModel)

	// Quick Validation

	if data == nil {

		repository.Error = error_constants.NilDataErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Reference = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.ReferenceList = make([]*pos_models.Reference, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	if id == uint(0) {

		repository.Error = error_constants.MissingIdErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Reference = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.ReferenceList = make([]*pos_models.Reference, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}
	if r.CheckIFExists(id).Reference.ID != data.ID {

		repository.Error = "Provided ResponseID to change is not the same as the ResponseID from the model or Entity object instance"
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.StatusCode = http.StatusInternalServerError
		repository.Reference = nil
		repository.ReferenceList = make([]*pos_models.Reference, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		return repository
	}

	data.Stage = "updated"
	createdAt := r.GetByID(data.ID).Reference.CreatedAt
	data.CreatedAt = createdAt
	data.UpdatedAt = time.Now()
	err := r.GormDB.Save(&data).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Reference = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.ReferenceList = make([]*pos_models.Reference, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}
	err = r.GormDB.Find(&repository.ReferenceList, "id=?", data.ID).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Reference = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.ReferenceList = make([]*pos_models.Reference, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	} else {

		if len(repository.ReferenceList) > 0 {
			repository.StatusCode = http.StatusOK
			repository.Message = "Success"
			repository.RepositoryStatus = true
			r.GormDB.Find(&repository.Reference, "id=?", repository.ReferenceList[0].ID)

		} else {
			repository.StatusCode = http.StatusNoContent
			repository.Message = "No Content"
			repository.RepositoryStatus = false
			repository.RepositoryErrorResponse.ErrorStatusCode = http.StatusNoContent
			repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		}
	}
	return repository
}
func (r *ReferenceRepository) AddOrUpdate(id uint, data *pos_models.Reference) (repository pos_models.ReferenceRepositoryResponse) {
	if r.CheckIFExists(id).RepositoryStatus {
		return r.Update(id, data)
	} else {
		return r.Add(data)
	}
}

