type ITransactionRepository interface {
	Add(data pos_models.Transaction) (repository pos_models.TransactionRepositoryResponse)
	Update(id uint, date pos_models.Transaction) (repository pos_models.TransactionRepositoryResponse)
	AddOrUpdate(id int, data pos_models.Transaction) (repository pos_models.TransactionRepositoryResponse)
	GetByID(id uint) (repository pos_models.TransactionRepositoryResponse)
	GetByName(param string) (repository pos_models.TransactionRepositoryResponse)
	GetByStage(param string) (repository pos_models.TransactionRepositoryResponse)
	GetByType(param string) (repository pos_models.TransactionRepositoryResponse)
	GetByDate(param string) (repository pos_models.TransactionRepositoryResponse)
	GetByStatus(param int) (repository pos_models.TransactionRepositoryResponse)
	GetByEnabled(param int) (repository pos_models.TransactionRepositoryResponse)
	GetByLocale(param string) (repository pos_models.TransactionRepositoryResponse)
	CheckIFExists(id uint) (repository pos_models.TransactionRepositoryResponse)
	GetAll() (repository pos_models.TransactionRepositoryResponse)
	Delete(id uint) (repository pos_models.TransactionRepositoryResponse)
}
type TransactionRepository struct {
	GormDB *gorm.DB
}

func New() *TransactionRepository {

	return &TransactionRepository{
		GormDB: pos_databases.New().DBConnection(),
	}
}

func (r *TransactionRepository) Add(data *pos_models.Transaction) (repository pos_models.TransactionRepositoryResponse) {
	repository.RepositoryErrorResponse = new(error_models.ErrorModel)

	if data == nil {

		repository.Error = error_constants.NilDataErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Transaction = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.TransactionList = make([]*pos_models.Transaction, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	if data.ID == uint(0) {

		repository.Error = error_constants.MissingIdErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Transaction = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.TransactionList = make([]*pos_models.Transaction, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	data.Stage = "added"
	data.CreatedAt = time.Now()
	log.Println(data.ToJSON())
	err := r.GormDB.Save(&data).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Transaction = data
		repository.StatusCode = http.StatusInternalServerError
		repository.TransactionList = make([]*pos_models.Transaction, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}
	err = r.GormDB.Find(&repository.TransactionList, "id=?", data.ID).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Transaction = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.TransactionList = make([]*pos_models.Transaction, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	if len(repository.TransactionList) > 0 {
		repository.StatusCode = http.StatusCreated
		repository.Message = "Success"
		repository.RepositoryStatus = true
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		r.GormDB.Find(&repository.Transaction, "id=?", repository.TransactionList[0].ID)

	} else {
		repository.StatusCode = http.StatusNoContent
		repository.Message = "No Content"
		repository.RepositoryStatus = false
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
	}
	return repository
}
func (r *TransactionRepository) Update(id uint, data *pos_models.Transaction) (repository pos_models.TransactionRepositoryResponse) {
	repository.RepositoryErrorResponse = new(error_models.ErrorModel)

	// Quick Validation

	if data == nil {

		repository.Error = error_constants.NilDataErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Transaction = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.TransactionList = make([]*pos_models.Transaction, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	if id == uint(0) {

		repository.Error = error_constants.MissingIdErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Transaction = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.TransactionList = make([]*pos_models.Transaction, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}
	if r.CheckIFExists(id).Transaction.ID != data.ID {

		repository.Error = "Provided ResponseID to change is not the same as the ResponseID from the model or Entity object instance"
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.StatusCode = http.StatusInternalServerError
		repository.Transaction = nil
		repository.TransactionList = make([]*pos_models.Transaction, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		return repository
	}

	data.Stage = "updated"
	createdAt := r.GetByID(data.ID).Transaction.CreatedAt
	data.CreatedAt = createdAt
	data.UpdatedAt = time.Now()
	err := r.GormDB.Save(&data).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Transaction = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.TransactionList = make([]*pos_models.Transaction, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}
	err = r.GormDB.Find(&repository.TransactionList, "id=?", data.ID).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Transaction = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.TransactionList = make([]*pos_models.Transaction, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	} else {

		if len(repository.TransactionList) > 0 {
			repository.StatusCode = http.StatusOK
			repository.Message = "Success"
			repository.RepositoryStatus = true
			r.GormDB.Find(&repository.Transaction, "id=?", repository.TransactionList[0].ID)

		} else {
			repository.StatusCode = http.StatusNoContent
			repository.Message = "No Content"
			repository.RepositoryStatus = false
			repository.RepositoryErrorResponse.ErrorStatusCode = http.StatusNoContent
			repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		}
	}
	return repository
}
func (r *TransactionRepository) AddOrUpdate(id uint, data *pos_models.Transaction) (repository pos_models.TransactionRepositoryResponse) {
	if r.CheckIFExists(id).RepositoryStatus {
		return r.Update(id, data)
	} else {
		return r.Add(data)
	}
}

