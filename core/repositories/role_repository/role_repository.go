func (r *RoleRepository) AddOrUpdate(id uint, data *auth_models.Role) (repository auth_models.RoleRepositoryResponse) {
	if r.CheckIFExists(id).RepositoryStatus {
		return r.Update(id, data)
	} else {
		return r.Add(data)
	}
}

