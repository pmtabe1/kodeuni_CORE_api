func (r *PermissionRepository) AddOrUpdate(id uint, data *auth_models.Permission) (repository auth_models.PermissionRepositoryResponse) {
	if r.CheckIFExists(id).RepositoryStatus {
		return r.Update(id, data)
	} else {
		return r.Add(data)
	}
}

