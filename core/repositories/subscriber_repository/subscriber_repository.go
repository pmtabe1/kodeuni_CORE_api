func (r *SubscriberRepository) AddOrUpdate(id uint, data *subscription_models.Subscriber) (repository subscription_models.SubscriberRepositoryResponse) {
	if r.CheckIFExists(id).RepositoryStatus {
		return r.Update(id, data)
	} else {
		return r.Add(data)
	}
}

