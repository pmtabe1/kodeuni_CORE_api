type INotificationRepository interface {
	Add(data subscription_models.Notification) (repository subscription_models.NotificationRepositoryResponse)
	Update(id uint, date subscription_models.Notification) (repository subscription_models.NotificationRepositoryResponse)
	AddOrUpdate(id int, data subscription_models.Notification) (repository subscription_models.NotificationRepositoryResponse)
	GetByID(id uint) (repository subscription_models.NotificationRepositoryResponse)
	GetByName(param string) (repository subscription_models.NotificationRepositoryResponse)
	GetByStage(param string) (repository subscription_models.NotificationRepositoryResponse)
	GetByType(param string) (repository subscription_models.NotificationRepositoryResponse)
	GetByDate(param string) (repository subscription_models.NotificationRepositoryResponse)
	GetByStatus(param int) (repository subscription_models.NotificationRepositoryResponse)
	GetByEnabled(param int) (repository subscription_models.NotificationRepositoryResponse)
	GetByLocale(param string) (repository subscription_models.NotificationRepositoryResponse)
	GetByNotificationID(param string) (repository subscription_models.NotificationRepositoryResponse)
	GetByClientID(param string) (repository subscription_models.NotificationRepositoryResponse)
	GetByToken(param string) (repository subscription_models.NotificationRepositoryResponse)
	GetByOwnerRef(param string) (repository subscription_models.NotificationRepositoryResponse)
	CheckIFExists(id uint) (repository subscription_models.NotificationRepositoryResponse)
	GetAll() (repository subscription_models.NotificationRepositoryResponse)
	Delete(id uint)
}

type NotificationRepository struct {
	GormDB *gorm.DB
}

func New() *NotificationRepository {

	return &NotificationRepository{
		GormDB: app_databases.New().DBConnection(),
	}
}

func (r *NotificationRepository) Add(data *subscription_models.Notification) (repository subscription_models.NotificationRepositoryResponse) {
	repository.RepositoryErrorResponse = new(error_models.ErrorModel)
	data.Stage = "added"
	err := r.GormDB.Save(&data).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Notification = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.NotificationList = make([]*subscription_models.Notification, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}
	err = r.GormDB.Find(&repository.NotificationList, "id=?", data.ID).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Notification = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.NotificationList = make([]*subscription_models.Notification, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	if len(repository.NotificationList) > 0 {
		repository.StatusCode = http.StatusCreated
		repository.Message = "Success"
		repository.RepositoryStatus = true
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		r.GormDB.Find(&repository.Notification, "id=?", repository.NotificationList[0].ID)

	} else {
		repository.StatusCode = http.StatusNoContent
		repository.Message = "No Content"
		repository.RepositoryStatus = false
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
	}
	return repository
}
func (r *NotificationRepository) Update(id uint, data *subscription_models.Notification) (repository subscription_models.NotificationRepositoryResponse) {
	repository.RepositoryErrorResponse = new(error_models.ErrorModel)

	// Quick Validation

	if r.CheckIFExists(id).Notification.ID != data.ID {

		repository.Error = "Provided ResponseID to change is not the same as the ResponseID from the model or Entity object instance"
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.StatusCode = http.StatusInternalServerError
		repository.Notification = nil
		repository.NotificationList = make([]*subscription_models.Notification, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		return repository
	}

	data.Stage = "updated"
	createdAt := r.GetByID(data.ID).Notification.CreatedAt
	data.CreatedAt = createdAt
	err := r.GormDB.Save(&data).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Notification = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.NotificationList = make([]*subscription_models.Notification, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}
	err = r.GormDB.Find(&repository.NotificationList, "id=?", data.ID).Error

	if err != nil {

		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Notification = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.NotificationList = make([]*subscription_models.Notification, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	} else {

		if len(repository.NotificationList) > 0 {
			repository.StatusCode = http.StatusOK
			repository.Message = "Success"
			repository.RepositoryStatus = true
			r.GormDB.Find(&repository.Notification, "id=?", repository.NotificationList[0].ID)
			repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
			repository.RepositoryErrorResponse.ErrorMessage = repository.Message

		} else {
			repository.StatusCode = http.StatusNoContent
			repository.Message = "No Content"
			repository.RepositoryStatus = false
			repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
			repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		}
	}
	return repository
}
func (r *NotificationRepository) AddOrUpdate(id uint, data *subscription_models.Notification) (repository subscription_models.NotificationRepositoryResponse) {
	if r.CheckIFExists(id).RepositoryStatus {
		return r.Update(id, data)
	} else {
		return r.Add(data)
	}
}

