type ISupplierRepository interface {
	Add(data pos_models.Supplier) (repository pos_models.SupplierRepositoryResponse)
	Update(id uint, date pos_models.Supplier) (repository pos_models.SupplierRepositoryResponse)
	AddOrUpdate(id int, data pos_models.Supplier) (repository pos_models.SupplierRepositoryResponse)
	GetByID(id uint) (repository pos_models.SupplierRepositoryResponse)
	GetByName(param string) (repository pos_models.SupplierRepositoryResponse)
	GetByStage(param string) (repository pos_models.SupplierRepositoryResponse)
	GetByType(param string) (repository pos_models.SupplierRepositoryResponse)
	GetByDate(param string) (repository pos_models.SupplierRepositoryResponse)
	GetByStatus(param int) (repository pos_models.SupplierRepositoryResponse)
	GetByEnabled(param int) (repository pos_models.SupplierRepositoryResponse)
	GetByLocale(param string) (repository pos_models.SupplierRepositoryResponse)
	CheckIFExists(id uint) (repository pos_models.SupplierRepositoryResponse)
	GetAll() (repository pos_models.SupplierRepositoryResponse)
	Delete(id uint) (repository pos_models.SupplierRepositoryResponse)
}
type SupplierRepository struct {
	GormDB *gorm.DB
}

func New() *SupplierRepository {

	return &SupplierRepository{
		GormDB: pos_databases.New().DBConnection(),
	}
}

func (r *SupplierRepository) Add(data *pos_models.Supplier) (repository pos_models.SupplierRepositoryResponse) {
	repository.RepositoryErrorResponse = new(error_models.ErrorModel)

	if data == nil {

		repository.Error = error_constants.NilDataErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Supplier = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.SupplierList = make([]*pos_models.Supplier, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	if data.ID == uint(0) {

		repository.Error = error_constants.MissingIdErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Supplier = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.SupplierList = make([]*pos_models.Supplier, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	data.Stage = "added"
	data.CreatedAt = time.Now()
	log.Println(data.ToJSON())
	err := r.GormDB.Save(&data).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Supplier = data
		repository.StatusCode = http.StatusInternalServerError
		repository.SupplierList = make([]*pos_models.Supplier, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}
	err = r.GormDB.Find(&repository.SupplierList, "id=?", data.ID).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Supplier = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.SupplierList = make([]*pos_models.Supplier, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	if len(repository.SupplierList) > 0 {
		repository.StatusCode = http.StatusCreated
		repository.Message = "Success"
		repository.RepositoryStatus = true
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		r.GormDB.Find(&repository.Supplier, "id=?", repository.SupplierList[0].ID)

	} else {
		repository.StatusCode = http.StatusNoContent
		repository.Message = "No Content"
		repository.RepositoryStatus = false
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
	}
	return repository
}
func (r *SupplierRepository) Update(id uint, data *pos_models.Supplier) (repository pos_models.SupplierRepositoryResponse) {
	repository.RepositoryErrorResponse = new(error_models.ErrorModel)

	// Quick Validation

	if data == nil {

		repository.Error = error_constants.NilDataErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Supplier = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.SupplierList = make([]*pos_models.Supplier, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}

	if id == uint(0) {

		repository.Error = error_constants.MissingIdErrorMessage
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Supplier = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.SupplierList = make([]*pos_models.Supplier, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}
	if r.CheckIFExists(id).Supplier.ID != data.ID {

		repository.Error = "Provided ResponseID to change is not the same as the ResponseID from the model or Entity object instance"
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.StatusCode = http.StatusInternalServerError
		repository.Supplier = nil
		repository.SupplierList = make([]*pos_models.Supplier, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode
		return repository
	}

	data.Stage = "updated"
	createdAt := r.GetByID(data.ID).Supplier.CreatedAt
	data.CreatedAt = createdAt
	data.UpdatedAt = time.Now()
	err := r.GormDB.Save(&data).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Supplier = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.SupplierList = make([]*pos_models.Supplier, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	}
	err = r.GormDB.Find(&repository.SupplierList, "id=?", data.ID).Error

	if err != nil {
		repository.Error = err.Error()
		repository.Message = "Error"
		repository.RepositoryStatus = false
		repository.Supplier = nil
		repository.StatusCode = http.StatusInternalServerError
		repository.SupplierList = make([]*pos_models.Supplier, 0)
		repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		repository.RepositoryErrorResponse.ErrorStackTrace = stacktrace_utils.GenerateStackstraceWithMessageCapture(errors.New(repository.Error))
		repository.RepositoryErrorResponse.ErrorStatusCode = repository.StatusCode

		return repository
	} else {

		if len(repository.SupplierList) > 0 {
			repository.StatusCode = http.StatusOK
			repository.Message = "Success"
			repository.RepositoryStatus = true
			r.GormDB.Find(&repository.Supplier, "id=?", repository.SupplierList[0].ID)

		} else {
			repository.StatusCode = http.StatusNoContent
			repository.Message = "No Content"
			repository.RepositoryStatus = false
			repository.RepositoryErrorResponse.ErrorStatusCode = http.StatusNoContent
			repository.RepositoryErrorResponse.ErrorMessage = repository.Message
		}
	}
	return repository
}
func (r *SupplierRepository) AddOrUpdate(id uint, data *pos_models.Supplier) (repository pos_models.SupplierRepositoryResponse) {
	if r.CheckIFExists(id).RepositoryStatus {
		return r.Update(id, data)
	} else {
		return r.Add(data)
	}
}

