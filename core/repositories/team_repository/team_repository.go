func (r *TeamRepository) AddOrUpdate(id uint, data *subscription_models.Team) (repository subscription_models.TeamRepositoryResponse) {
	if r.CheckIFExists(id).RepositoryStatus {
		return r.Update(id, data)
	} else {
		return r.Add(data)
	}
}

