func (r *ConsumptionRepository) AddOrUpdate(id uint, data *pos_models.Consumption) (repository pos_models.ConsumptionRepositoryResponse) {
	if r.CheckIFExists(id).RepositoryStatus {
		return r.Update(id, data)
	} else {
		return r.Add(data)
	}
}

